class InvalidInputException(Exception):
    """Исключение для недопустимых входных данных функции.
    
    Используется, когда переданные аргументы не соответствуют
    области определения математической функции.
    """
    
    def __init__(self, f: callable, *args):
        """Инициализирует исключение с информацией о функции и аргументах.
        
        Аргументы:
            f: функция, в которой возникла ошибка
            *args: аргументы, которые вызвали ошибку
        """
        super().__init__()  # Инициализируем базовый класс Exception
        self.f = f         # Сохраняем проблемную функцию
        self.args = args   # Сохраняем недопустимые аргументы

    def __str__(self) -> str:
        """Возвращает читаемое сообщение об ошибке.
        
        Возвращает:
            Строку с описанием ошибки в формате:
            "Values (аргументы) violate (имя функции)'s function domain"
        """
        return f"Values {self.args} violate {self.f.__name__}'s function domain"
